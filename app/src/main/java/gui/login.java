    private void jButton1MouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseReleased
        DBConnection db = new DBConnection();
        try {
            if (userText.getText() != null && passText.getText() != null) {
                ResultSet rs = db.dbQuery("Select * from users Where username='" + userText.getText() + "' and password='" + passText.getText() + "'");
                if(rs.next()) {
                    JOptionPane.showMessageDialog(null, "Login efetuado com sucesso!");
                    User userlogged = new User(rs.getString("username"), rs.getString("password"), rs.getString("name"), rs.getString("profile"));
                    new main(userlogged).setVisible(true);
                    this.setVisible(false);
                    
                } else {
                    JOptionPane.showMessageDialog(null, "Dados incorretos!");
                }
            }
        } catch (SQLException err) {
            JOptionPane.showMessageDialog(null, err.getMessage());
        }
    }//GEN-LAST:event_jButton1MouseReleased

